function [ Abar ] = A2Abar( A )
% This Function transforms A matrices to Abar in order to evaluate 6X6
% transformation matrices

Abar=[A(1,1)*A(1,1) A(1,2)*A(1,2) A(1,3)*A(1,3) (2)^0.5*A(1,2)*A(1,3) ...
    (2)^0.5*A(1,1)*A(1,3) (2)^0.5*A(1,1)*A(1,2);
    A(2,1)*A(2,1) A(2,2)*A(2,2) A(2,3)*A(2,3) (2)^0.5*A(2,2)*A(2,3) ...
    (2)^0.5*A(2,1)*A(2,3) (2)^0.5*A(2,1)*A(2,2);
    A(3,1)*A(3,1) A(3,2)*A(3,2) A(3,3)*A(3,3) (2)^0.5*A(3,2)*A(3,3) ...
    (2)^0.5*A(3,1)*A(3,3) (2)^0.5*A(3,1)*A(3,2);
    (2)^0.5*A(2,1)*A(3,1) (2)^0.5*A(2,2)*A(3,2) (2)^0.5*A(2,3)*A(3,3) ...
    A(2,2)*A(3,3)+A(2,3)*A(3,2) A(2,1)*A(3,3)+A(2,3)*A(3,1) ... 
    A(2,1)*A(3,2)+A(2,2)*A(3,1);
    (2)^0.5*A(1,1)*A(3,1) (2)^0.5*A(1,2)*A(3,2) (2)^0.5*A(1,3)*A(3,3) ...
    A(1,2)*A(3,3)+A(1,3)*A(3,2) A(1,1)*A(3,3)+A(1,3)*A(3,1) ...
    A(1,1)*A(3,2)+A(1,2)*A(3,1);
    (2)^0.5*A(1,1)*A(2,1) (2)^0.5*A(1,2)*A(2,2) (2)^0.5*A(1,3)*A(2,3) ...
    A(1,2)*A(2,3)+A(1,3)*A(2,2) A(1,1)*A(2,3)+A(1,3)*A(2,1) ...
    A(1,1)*A(2,2)+A(1,2)*A(2,1)];

end

